class Calc:
    def plus(self, x, y):
        print(x + y)

    def minus(self, x, y):
        print(x - y)

    def mul(self, x, y):
        print(x * y)

    def div(self, x, y):
        print(x / y)

casio = Calc()
casio.plus(4, 5)
casio.minus(16, 5)
casio.mul(2, 7)
casio.div(5, 2)